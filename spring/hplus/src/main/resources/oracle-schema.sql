CREATE TABLE CUSTOMERS (
    CUSTOMER_ID NUMBER GENERATED ALWAYS AS IDENTITY(START WITH 1 INCREMENT BY 1),
    FIRST_NAME VARCHAR(64),
    LAST_NAME VARCHAR(64),
    EMAIL VARCHAR(128) UNIQUE,
    PHONE VARCHAR(32),
    ADDRESS VARCHAR(256),
    CITY VARCHAR(64),
    STATE CHAR(2),
    ZIPCODE VARCHAR(12)
);
alter table customers modify  customer_id generated by default as identity;
alter table customers modify  customer_id primary key;
CREATE TABLE SALESPEOPLE (
    SALESPERSON_ID NUMBER  GENERATED ALWAYS as IDENTITY(start with 1 increment by 1),
    FIRST_NAME VARCHAR(64),
    LAST_NAME VARCHAR(64),
    EMAIL VARCHAR(128) UNIQUE,
    PHONE VARCHAR(32),
    ADDRESS VARCHAR(256),
    CITY VARCHAR(64),
    STATE CHAR(2),
    ZIPCODE VARCHAR(12),
    CONSTRAINT pk_salesperson_ PRIMARY KEY(salesperson_id)
);
CREATE TABLE PRODUCTS (
    PRODUCT_ID VARCHAR(32) PRIMARY KEY,
    NAME VARCHAR(128),
    MEASURE INT,
    VARIETY VARCHAR(32),
    PRICE NUMERIC(4,2),
    STATUS VARCHAR(16)
);

CREATE TABLE ORDERS (
    ORDER_ID VARCHAR(16) PRIMARY KEY,
    CUSTOMER_ID NUMBER,
    SALESPERSON_ID NUMBER
);
CREATE TABLE ORDER_LINES(
    ORDER_LINE_ID Number generated by default as identity,
    ORDER_ID VARCHAR(16),
    PRODUCT_ID VARCHAR(32),
    QUANTITY INT
);
